# OPERATORS IN PYTHON

# Arithmetic operators:
#     +	Addition	       x + y	
#     -	Subtraction	       x - y	
#     *	Multiplication	   x * y	
#     /	Division	       x / y	(returns a float)
#     %	Modulus	           x % y	
#    **	Exponentiation	   x ** y	
#    //	Floor division	   x // y   (returns the nearest integer value)

# Assignment operators:
#     Operator	       Example	              Same As	
#        =	            x = 5	               x = 5	
#       +=	            x += 3	               x = x + 3	
#       -=	            x -= 3	               x = x - 3	
#       *=	            x *= 3	               x = x * 3	
#       /=	            x /= 3	               x = x / 3	
#       %=	            x %= 3	               x = x % 3	
#       //=	            x //= 3	               x = x // 3	
#       **=	            x **= 3	               x = x ** 3	
#       &=	            x &= 3	               x = x & 3	
#       |=	            x |= 3	               x = x | 3	
#       ^=	            x ^= 3	               x = x ^ 3	
#       >>=	            x >>= 3	               x = x >> 3	
#       <<=	            x <<= 3	               x = x << 3	
#       :=	            print(x := 3)	       x = 3;  print(x) ... assign the value to a variable
#

# Comparison operators:
